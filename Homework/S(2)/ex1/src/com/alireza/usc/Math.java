package com.alireza.usc;

public class Math  {
    float firstNumber;
    float secondNumber ;


    float sum(){
        float sum = firstNumber + secondNumber;
        return sum ;
    }
    float multiply(){
        float multiply = firstNumber * secondNumber;

        return multiply;
    }
    float subtract(){
       float subtract = firstNumber - secondNumber;

       return subtract;

    }
    float division(){
        float division = (float) firstNumber/secondNumber;

        return division;
    }















}
